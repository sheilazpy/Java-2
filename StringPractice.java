// The "StringPractice" class.
public class StringPractice
{
    public static void main (String[] args)
    {
	int i = 10, j = 25;
	double x = 3.14159, y = -77.5;
	String s = "Hello", space = " ";
	System.out.println (i + space + j + space + j + i);
	System.out.println (s + space + x);
	System.out.print (y);
	System.out.println (y + space + s + space + y);
    } // main method
} // StringPractice class
