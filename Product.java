// The "Product" class.
public class Product
{
    public static void main (String[] args)
    {
	int m1, m2, product;
	m1 = 3;
	m2 = 4;
	product = 12;
	System.out.print (m1);
	System.out.print (" * ");
	System.out.println (m2);
	System.out.print (" = ");
	System.out.println (product);
    } // main method
} // Product class
